function testingSwitch() {
    var nilai = "Dilo";

    switch (nilai) {
        case "Dilo":
            console.log("Sangat bagus");
            break;
        case 8:
            console.log("Baik");
            break;
        case 6:
            console.log("Cukup");
            break;
        default:
            console.log("Lainnya")
            break;
    }
}

testingSwitch();